function sum(a:number, b:number):number {
    return a + b;
};

function substr(a:number, b:number):number {
    return a - b;
};

function division(a:number, b:number):number {
    return a / b;
};

function multi(a:number, b:number):number {
    return a * b;
};

export default {
    sum,
    substr,
    division,
    multi
};